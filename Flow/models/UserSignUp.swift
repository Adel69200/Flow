import Foundation


struct UserSignUp: Identifiable {
    let id = UUID()
    let name: String
    let email: String
    let password : String
  
    
    
    
    enum SubscriptionError: Error {
        case invalidUsername
        case invalidPassword
        case invalidEmail
        case passwordsNotMatching
    }
    
   
    
    
}
